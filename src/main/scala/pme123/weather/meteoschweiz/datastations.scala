// Helper function to convert MeteoSwissStation to WeatherStation
def toWeatherStation(station: MeteoSwissStation): WeatherStation =
  WeatherStation(station.name, station.coordinatesWgs84Lat, station.coordinatesWgs84Lon)

// Convert MeteoSwiss stations to WeatherStation format
lazy val meteoSwissWeatherStations: Set[WeatherStation] =
  meteoSwissStations.map(toWeatherStation)

def station(abbreviation: String) =
  meteoSwissStations
    .find(_.abbr == abbreviation).map(toWeatherStation)
    .getOrElse(throw new Exception(s"Station $abbreviation not found"))
end station
